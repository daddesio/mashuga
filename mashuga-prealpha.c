static void client_handle_transmit_datablob_pdu(struct Client *client, uint8_t *voltron_data, unsigned voltron_size)
{
	/* Forward the data blob to the other party via a BroadcastDataBlobPDU.
	**
	** Note that Pre-Alpha ignores TransmitDataBlobPDU (that is, it parses the packet and
	** instantly frees it).
	** BroadcastDataBlobPDU, however, works. Its callback function is
	** TSOVoltronDMServiceD_base+0x3274. In response to BroadcastDataBlobPDU, Pre-Alpha
	** posts the contained cTSONetMessageStandard to its message queue.
	**
	** Hence, in response to TransmitDataBlobPDU, the city server should read the recipient
	** from the packet, convert the packet to a BroadcastDataBlobPDU, and forward it
	** to the recipient.
	**
	** Converting TransmitDataBlobPDU to BroadcastDataBlobPDU simply involves
	** deleting the two additional recipient strings in the TransmitDataBlobPDU header
	** and then updating the PDU code from 0x0081 to 0x0080.
	*/

	struct Client *dest_client = client->is_roomserver ? &player : &roomserver;
	uint32_t outgoing_voltron_size = voltron_size - 14;
	uint8_t aries_header[12] = {0};

	printf("Reached client_handle_transmit_datablob_pdu.\n");
	WRITE32LE(aries_header+8, outgoing_voltron_size);

	WRITE16BE(voltron_data, 0x0080);
	WRITE32BE(voltron_data+2, outgoing_voltron_size);

	/* Delete the 14 bytes at offset 28 in the PDU. */
	memmove(voltron_data+28, voltron_data+28+14, outgoing_voltron_size-28);

	write_socket(dest_client->fd, aries_header, 12);
	write_socket(dest_client->fd, voltron_data, outgoing_voltron_size);
}

static void client_handle_chat_msg_pdu(struct Client *client, uint8_t *voltron_data, unsigned voltron_size)
{
	uint8_t aries_header[12] = {0};

	printf("Reached client_handle_chat_msg_pdu.\n");
	WRITE32LE(aries_header+8, voltron_size);
	write_socket(roomserver.fd, aries_header, 12);
	write_socket(roomserver.fd, voltron_data, voltron_size);
	write_socket(player.fd, aries_header, 12);
	write_socket(player.fd, voltron_data, voltron_size);
}

static void handle_voltron_packet(struct Client *client, uint8_t *voltron_data, unsigned voltron_size)
{
	uint16_t type = READ16BE(voltron_data);

	printf("Received 0x%04X packet from %s.\n",
		(unsigned)type, client->is_roomserver ? "room server" : "owner");

	fprintf(logfd, "\nReceived %u bytes from %s:\n",
		voltron_size, client->is_roomserver ? "room server" : "owner");
	fwrite(voltron_data, 1, voltron_size, logfd);

	switch (type) {
	case 0x0099: client_handle_load_house_response_pdu(client, voltron_data, voltron_size); break;
	case 0x0082: client_handle_db_request_wrapper_pdu(client, voltron_data, voltron_size); break;
	case 0x0089: client_handle_lot_entry_request_pdu(client, voltron_data, voltron_size); break;
	case 0x0080: client_handle_broadcast_datablob_pdu(client, voltron_data, voltron_size); break;
	case 0x0081: client_handle_transmit_datablob_pdu(client, voltron_data, voltron_size); break;
	case 0x0008: client_handle_chat_msg_pdu(client, voltron_data, voltron_size); break;
	/* TODO: In response to SplitBufferPDU etc., fail (or warn) with "Error: unsupported pdu". */
	}
}

