static void read_socket(int fd, uint8_t *data, uint32_t size)
{
	if (recv(fd, (char*)data, size, MSG_WAITALL) != (int)size) {
		fprintf(stderr, "error: recv failed\n");
		exit(EXIT_FAILURE);
	}
}

static void write_socket(int fd, const uint8_t *data, uint32_t size)
{
	/* Unlike recv, send does not support MSG_WAITALL. */
	while (size > 0) {
		ssize_t ret = send(fd, (const char*)data, size, 0);
		if (ret <= 0) {
			fprintf(stderr, "error: send failed\n");
			exit(EXIT_FAILURE);
		}

		data += ret;
		size -= ret;
	}
}

