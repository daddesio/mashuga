static void handle_socket_data(struct Client *client)
{
	static uint8_t packet_data[MAX_ARIES_SIZE];
	uint32_t packet_type, payload_size, position;

	read_socket(client->fd, packet_data, 12);

	packet_type = READ32LE(packet_data);
	payload_size = READ32LE(packet_data+8);
	if (payload_size > MAX_ARIES_SIZE) {
		fprintf(stderr, "error: invalid packet size\n");
		exit(EXIT_FAILURE);
	}

	read_socket(client->fd, packet_data, payload_size);

	if (packet_type != 0)
		return;

	position = 0;
	while (payload_size - position >= 6) {
		uint32_t voltron_size = READ32BE(packet_data+position+2);
		if (voltron_size < 6 || voltron_size > payload_size - position) {
			fprintf(stderr, "error: invalid voltron packet size\n");
			exit(EXIT_FAILURE);
		}

		handle_voltron_packet(client, packet_data+position, voltron_size);
		position += voltron_size;
	}
}

static int wait_for_connection(const char *name, uint16_t port)
{
	struct sockaddr_in sa;
	socklen_t sa_len;
	int listenfd, sockfd;

	listenfd = socket(AF_INET, SOCK_STREAM, 0);
	if (listenfd < 0) {
		fprintf(stderr, "error: failed to create listening socket\n");
		exit(EXIT_FAILURE);
	}

	memset(&sa, 0, sizeof(sa));
	sa.sin_family = AF_INET;
	sa.sin_port = htons(port);
	sa.sin_addr.s_addr = INADDR_ANY;

	if (bind(listenfd, (struct sockaddr *)&sa, sizeof(sa)) < 0) {
		fprintf(stderr, "error: bind failed\n");
		exit(EXIT_FAILURE);
	}

	if (listen(listenfd, 0) < 0) {
		fprintf(stderr, "error: listen failed\n");
		exit(EXIT_FAILURE);
	}

	printf("Waiting for the %s to connect (port %u)...\n", name, (unsigned)port);

	sa_len = sizeof(sa);
	sockfd = accept(listenfd, (struct sockaddr *)&sa, &sa_len);
	if (sockfd < 0) {
		fprintf(stderr, "error: accept failed\n");
		exit(EXIT_FAILURE);
	}

	close(listenfd);
	return sockfd;
}

int main()
{
	int nfds;
	fd_set readfds;

	logfd = fopen("log.dat", "wb");
	if (!logfd) {
		fprintf(stderr, "error: failed to open log.dat for writing\n");
		return EXIT_FAILURE;
	}

	setbuf(logfd, NULL);

#ifdef _WIN32
	if (WSAStartup(MAKEWORD(2, 2), &wsaData) != 0) {
		fprintf(stderr, "error: WSAStartup failed\n");
		return EXIT_FAILURE;
	}
#endif

	roomserver.is_roomserver = 1;
	player.is_roomserver = 0;

	roomserver.fd = wait_for_connection("room server", 49101);
	player.fd = wait_for_connection("owner", 49100);

	nfds = 1+MAX(roomserver.fd, player.fd);
	if (nfds > FD_SETSIZE) {
		fprintf(stderr, "error: fd too large for select\n");
		return EXIT_FAILURE;
	}

	printf("Both parties have arrived. Press enter to start.\n");
	getchar();

	roomserver_handle_connection_established(&roomserver);
	player_handle_connection_established(&player);

	while (1) {
		FD_ZERO(&readfds);
		FD_SET(roomserver.fd, &readfds);
		FD_SET(player.fd, &readfds);
		if (select(nfds, &readfds, NULL, NULL, NULL) < 0) {
			fprintf(stderr, "error: select failed\n");
			return EXIT_FAILURE;
		}

		if (FD_ISSET(roomserver.fd, &readfds))
			handle_socket_data(&roomserver);
		if (FD_ISSET(player.fd, &readfds))
			handle_socket_data(&player);
	}

	return EXIT_SUCCESS;
}
